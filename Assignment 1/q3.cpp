#include <iostream>
using namespace std;
int main() {
    int a=10;
    int b=5;
    int add= a+b;
    int sub= a-b;
    int mul= a*b;
    int div= a/b;
    cout<<"The sum of numbers are: "<<add<<endl;
    cout<<"The subtraction of numbers are: "<<sub<<endl;
    cout<<"The multiplication of numbers are: "<<mul<<endl;
    cout<<"The division of numbers are: "<<div<<endl;
    return 0;
}
